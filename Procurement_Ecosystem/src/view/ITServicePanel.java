    private void btnCreateOrgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateOrgActionPerformed
        String orgName = cmbOrganization.getSelectedItem().toString();
        OrganizationType orgType = UIUtil.getSelectedEnum(cmbOrganization, OrganizationType.class);

        if (orgType == null) {
            UIUtil.showError(this, "Organization name cannot be empty");
            return;
        }

        network.getOrgService().createOrgFromEnterprise(orgType, enterprise);
        reloadOrgTable();
    }//GEN-LAST:event_btnCreateOrgActionPerformed

