    private void reloadOrgTable() {
        // Load all organization within this Enterprise
        UIUtil.reloadTable(
                tblOrg,
                enterprise.getOrganizationDir().getOrganizationList(),
                org -> new Object[]{
                        org.getTypeName(),
                }
        );
    }

